function verificaResposta(resposta){
    if (resposta === "b"){
        resultado.innerHTML = "Resposta correta";
        resultado.style.color = "green"
    } else{
        resultado.innerHTML = "Resposta incorreta";
        resultado.style.color = "red"
    }
}

